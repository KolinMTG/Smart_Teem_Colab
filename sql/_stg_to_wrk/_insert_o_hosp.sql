INSERT INTO BASE_WORK.O_HOSP (
    HOSP_ID,
    CONS_ID,
    ROOM_NUM,
    HOSP_STRT_DTTM,
    HOSP_END_DTTM,
    HOSP_FINL_RATE,
    STFF_ID,
    EXEC_ID
)
SELECT
    h.ID_HOSPI AS HOSP_ID,
    h.ID_CONSULT AS CONS_ID,
    h.NO_CHAMBRE AS ROOM_NUM,
    h.TS_DEBUT_HOSPI AS HOSP_STRT_DTTM,
    h.TS_FIN_HOSPI AS HOSP_END_DTTM,
    h.COUT_HOSPI AS HOSP_FINL_RATE,
    rp.PART_ID AS STFF_ID,
    %s AS EXEC_ID
FROM BASE_STAGING.HOSPITALISATION h
LEFT JOIN BASE_WORK.R_PART rp
    ON h.ID_PERSONNEL_RESP = rp.SRC_ID
   AND rp.SRC_TYP <> 'Patient'; 
